public class matematika {
    int hasilnya;
    
    void pertambahan(int x,int y){
        System.out.println(x + " + " + y + " = " + (x+y));
    }
    void Pengurangan(int x,int y){
        System.out.println(x + " - " + y + " = " + (x-y));
    }
    void perkalian(int x,int y){
        System.out.println(x + " x " + y + " = " + (x*y));
    }
    void pembagian(int x,int y){
        System.out.println(x + " / " + y + " = " + (x/y));
    }
}
